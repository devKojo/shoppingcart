

function openBtn() {
    let popcover = document.getElementById("pop-cover");
    let popup = document.getElementById("popup-box");
    popcover.style.visibility = "visible";
    popup.style.visibility = "visible";
}


function closeBtn() {
    let popcover = document.getElementById("pop-cover");
    let popup = document.getElementById("popup-box");
     popcover.style.visibility = "hidden";
    popup.style.visibility = "hidden";
}